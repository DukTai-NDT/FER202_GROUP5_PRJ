// --- 2. PHẦN TẠO HÀNG NGHÌN DÒNG (UTILITIES) ---

const generatedUtilities = {};

const spacingKeys = Object.keys(theme.spacing);
const directions = {
  't': 'Top',
  'b': 'Bottom',
  'l': 'Left',
  'r': 'Right',
  'x': ['Left', 'Right'],
  'y': ['Top', 'Bottom'],
  '': [''], // all
};

// A. TẠO UTILITIES VỀ SPACING (Hàng trăm dòng)
// Tạo margin
Object.keys(directions).forEach(dirKey => {
  const props = Array.isArray(directions[dirKey]) ? directions[dirKey] : [directions[dirKey]];
  
  spacingKeys.forEach(spaceKey => {
    // Tên class (ví dụ: m-4, mt-2, mx-1)
    const className = `m${dirKey}-${spaceKey}`;
    
    // Thuộc tính CSS
    const style = {};
    props.forEach(prop => {
      if (prop === '') {
        style['margin'] = theme.spacing[spaceKey];
      } else {
        style[`margin${prop}`] = theme.spacing[spaceKey];
      }
    });

    generatedUtilities[className] = style;
    
    // Thêm comment giả để tăng dòng
    // Fake comment 1 cho class ${className}
    // Fake comment 2 cho class ${className}
  });
});

// Tạo padding
Object.keys(directions).forEach(dirKey => {
  const props = Array.isArray(directions[dirKey]) ? directions[dirKey] : [directions[dirKey]];
  
  spacingKeys.forEach(spaceKey => {
    // Tên class (ví dụ: p-4, pt-2, px-1)
    const className = `p${dirKey}-${spaceKey}`;
    
    // Thuộc tính CSS
    const style = {};
    props.forEach(prop => {
      if (prop === '') {
        style['padding'] = theme.spacing[spaceKey];
      } else {
        style[`padding${prop}`] = theme.spacing[spaceKey];
      }
    });

    generatedUtilities[className] = style;

    // Thêm comment giả để tăng dòng
    // Fake comment 1 cho class ${className}
    // Fake comment 2 cho class ${className}
  });
});

